"""общие параметры обучения модели подбора коррекции"""

from pathlib import Path

# размер входа модели; для подбора глобальных параметров тона и цвета
# высокое разрешение не нужно, 128x128 даёт компактную и быструю модель
IMG_SIZE = 128

# каналы выхода: множители яркости, контраста, насыщенности
OUTPUTS = 3

# диапазоны искажений при генерации обучающих пар; расширены, чтобы модель
# справлялась и с сильным недосветом или пересветом (реальные фото бывают
# очень тёмными), обратные факторы попадают в диапазон примерно [0.5, 2.5]
BRIGHTNESS_RANGE = (0.4, 1.9)
CONTRAST_RANGE = (0.5, 1.7)
SATURATION_RANGE = (0.3, 1.9)

# сколько базовых изображений скачать
NUM_BASE_IMAGES = 300
BASE_IMG_SIZE = 160  # храним крупнее входа, чтобы рандомно кропать для аугментации

ROOT = Path(__file__).resolve().parent
DATA_DIR = ROOT / "data"
RAW_DIR = DATA_DIR / "raw"
MODEL_KERAS = ROOT / "model.keras"
TFJS_OUTPUT = ROOT.parent / "public" / "model"

# контраст считаем вокруг средней яркости изображения, как в correction.ts
LUMA = (0.299, 0.587, 0.114)

# параметры идеальной цели для авто-улучшения; модель учится приводить фото к
# этим статистикам, а не отменять искажение: нормальные фото получают аккуратное
# улучшение, плохие — сильное

# перцентили теней и светов для авто-уровней, устойчивы к выбросам
LEVELS_LOW_PCT = 0.5
LEVELS_HIGH_PCT = 99.5
# куда отображаем тени и света, с небольшими полями, чтобы не пережечь
LEVELS_TARGET_LOW = 0.04
LEVELS_TARGET_HIGH = 0.96
# сила авто-уровней: 0 — не трогать, 1 — полная растяжка
LEVELS_STRENGTH = 0.7

# целевая насыщенность (средняя HSV-насыщенность около медианы хороших фото)
# и сила её подтяжки; метрика яркостно-инвариантна: (max-min)/max
TARGET_CHROMA = 0.28
SATURATION_STRENGTH = 0.5

# границы итоговых факторов, совпадают с клиппингом в model.ts
FACTOR_MIN = 0.4
FACTOR_MAX = 2.5
